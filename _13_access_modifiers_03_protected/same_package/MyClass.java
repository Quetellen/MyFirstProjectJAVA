package com.simplilearn.fsc._13_access_modifiers_03_protected.same_package;

/*
 * The protected access modifier only applies to properties and methods
 * this access modifier it is the same as the default access modifier
 */

public class MyClass {

	protected int num = 4;

	protected void doSomething(){
		System.out.println("Doing Something");
	}

}
